package mit;

public class Num {
	int id;
	
	public int a,b;
	
	Num(int a, int b) {
		this.a = a;
		this.b = b;
	}
	
	Num() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}
	
	
}
